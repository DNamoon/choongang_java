
public class EmpData {
	//id,name,email,salary
	int emp_id;
	String emp_name;
	String emp_email;
	int emp_salary;
	
	EmpData(int emp_id, String emp_name, String emp_email, int emp_salary){
		this.emp_id = emp_id;
		this.emp_name = emp_name;
		this.emp_email = emp_email;
		this.emp_salary = emp_salary;
	}
	
	
}
